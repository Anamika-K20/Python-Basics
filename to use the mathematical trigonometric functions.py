#Write a Python program to use the mathematical trigonometric functions.
import math
print("Value of acos(0.5)=",math.acos(0.5))
print("Value of asin(0.5)=",math.asin(0.5))
print("Value of atan(0.5)=",math.atan(0.5))
print("Value of atan2(0.5,0.1)=",math.atan2(0.5,0.1))
print("Value of cos(0.5)=",math.cos(0.5))
print("Value of sin(0.5)=",math.sin(0.5))
print("Value of tan(0.5)=",math.tan(0.5))
print("Value of degrees(10)=",math.degrees(10))
print("Value of radians(10)=",math.radians(10))
